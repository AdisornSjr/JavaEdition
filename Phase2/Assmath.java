import java.lang.Math;
import java.lang.Math;
public class Assmath {
    public static void main(String[] args) {
        //ค่าคงที่ class math
        System.out.println(Math.PI); //PI
        System.out.println(Math.E); //log e

        //abs
        int a = -30;
        System.out.println(Math.abs(a));

        //ปัด
        double num = 15.5;
        System.out.println(Math.ceil(num)); //ปัดขัี้น
        System.out.println(Math.floor(num)); //ปัดลง
        //> 0.5
        // <= 0.5
        System.out.println(Math.rint(num));

        //root
        System.out.println("root 2 of 9 = "+Math.sqrt(9));

        //pow
        System.out.println("5 pow 2 = "+Math.pow(5, 2));


    }
    
   
}
